package com.test.memory.vo;

public class NoteImgVO {
	private int noteImgNo;
	private int noteNo;
	private String noteImgPath;
	private int noteImgWidth;
	private int noteImgHeight;
	
	public int getNoteImgNo() {
		return noteImgNo;
	}
	public void setNoteImgNo(int noteImgNo) {
		this.noteImgNo = noteImgNo;
	}
	public int getNoteNo() {
		return noteNo;
	}
	public void setNoteNo(int noteNo) {
		this.noteNo = noteNo;
	}
	public String getNoteImgPath() {
		return noteImgPath;
	}
	public void setNoteImgPath(String noteImgPath) {
		this.noteImgPath = noteImgPath;
	}
	public int getNoteImgWidth() {
		return noteImgWidth;
	}
	public void setNoteImgWidth(int noteImgWidth) {
		this.noteImgWidth = noteImgWidth;
	}
	public int getNoteImgHeight() {
		return noteImgHeight;
	}
	public void setNoteImgHeight(int noteImgHeight) {
		this.noteImgHeight = noteImgHeight;
	}
}
